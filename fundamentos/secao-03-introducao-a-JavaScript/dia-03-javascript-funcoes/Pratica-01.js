let balance = 10000
function addBalance(addAmountBalace) {
    return balance += addAmountBalace;
}
function subtractBalance(subtractAmountBalance) {
    return balance -= subtractAmountBalance;
}
function multiplyBalance(multiplyAmountBalance) {
    return balance = balance * multiplyAmountBalance;
}
function divideBalance(divideAmountBalance) {
    return balance = balance / divideAmountBalance;
}